// @lc code=start
function climbStairs(n: number): number {
    n = n + 1
    return fib(n)
};

function fib(n: number): number {
    let a = 1;
    let b = 1;
    for (let i = 3; i <= n; i++) {
        let c = a + b;
        a = b;
        b = c;
    }
    return b;
}
